func display(field [21][61]int) {
	cmd := exec.Command("cmd", "/c", "cls")
	cmd.Stdout = os.Stdout
	cmd.Run()
	for i := 0; i < 21; i++ {
		for j := 0; j < 61; j++ {
			if field[i][j] == 1 {
				c := fmt.Sprintf("\x1b[%dm%s\x1b[0m", 97, "█")
				fmt.Print(c)
			} else if field[i][j] == 2 {
				c := fmt.Sprintf("\x1b[%dm%s\x1b[0m", 31, "▓")
				fmt.Print(c)
			} else {
				fmt.Print(" ")
			}
		}
		fmt.Println("")
	}
	fmt.Println("")
}

func find(field *[21][61]int, used [21][61]bool, x int, y int) {
	queueX := make([]int, 0)
	queueY := make([]int, 0)
	queueX = append(queueX, x)
	queueY = append(queueY, y)
	cnt := 0
	for len(queueX) > 0 && cnt < 100 {
		curX := queueX[0]
		curY := queueY[0]
		queueX = queueX[1:]
		queueY = queueY[1:]

		if curX >= 0 && curY >= 0 && curX < 21 && curY < 61 && !used[curX][curY] && field[curX][curY] == 0 {
			field[curX][curY] = 2
			used[curX][curY] = true

			queueX = append(queueX, curX+1)
			queueY = append(queueY, curY)

			queueX = append(queueX, curX-1)
			queueY = append(queueY, curY)

			queueX = append(queueX, curX)
			queueY = append(queueY, curY+1)

			queueX = append(queueX, curX)
			queueY = append(queueY, curY-1)

			cnt++

			//display(*field)
			time.Sleep(time.Millisecond * 50)
		}
	}
}

